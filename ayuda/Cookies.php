<h4>Cookies</h4>

<div class="entry-content">

<p>Una cookie es un pequeño fragmento de texto que los sitios web que visitas envían al navegador y que permite que
el sitio web recuerde información sobre tu visita, como tu idioma preferido y otras opciones, lo que puede facilitar
tu próxima visita y hacer que el sitio te resulte más útil. Las cookies desempeñan un papel muy importante, ya que
sin ellas el uso de la Web sería una experiencia mucho más frustrante.</p>
<p>CromosRepes.com utiliza cookies con diversos fines, entre los que se incluyen recordar tus preferencias, aumentar la
relevancia de los anuncios que ves, contar el número de visitas que recibimos para acceder a una página, ayudarte a
registrarte en nuestros servicios y proteger tus datos.</p>
