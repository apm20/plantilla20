<p>Aquí están las preguntas más frecuentes organizadas por los siguientes temas:</p>
<p><strong>L. Listas de Faltas y Repes</strong></p>
<p><em>L01. ¿Como utilizo la página en un Tablet o  Móvil ya que no tienen botón derecho?</em></p>
<p>Puedes utilizar la nueva función de Listas Numeradas y meter los cromos tecleando los números.</p>
<p>Para las listas clásicas si el móvil/tablet es Android y el navegador es Chrome, si haces una pulsación breve siempre aumenta el número de faltas o repes, pero si mantienes un <strong>rato</strong> el dedo sobre una casilla podrás teclear un número sobre la misma, incluso cero.</p>
<p>En las tabletas o telefónos que no funcione dicha pulsación prolongada puedes desactivar las dos checkbox de la parte superior que tiene la siguiente leyenda:</p>
<p>(+) Añadir cromo con Botón Izquierdo <br />
  (-) Quitar cromo con Botón Derecho del ratón</p>
<p>Asi no aumentarán los cromos al pulsar sobre la casilla y podrás teclear el número que necesites.</p>
<p><strong>C. Cambios</strong></p>
<p><em>C01. ¿Cuando se considera cerrado un intercambio?</em></p>
<blockquote>
<p>El usuario A hace una oferta al usuario B. Si el usuario B acepta el cambio y envía sus datos personales, <strong>el cambio está cerrado</strong>. </p>
<ul>
  <li>Si el usuario A no envía sus propios datos de envío en un plazo de <strong>48 horas</strong> después de que el cambio sea aceptado, el usuario B podrá hacer una evaluación neutra (0) al usuario A.</li>
  <li>Si el usuario A rechaza el cambio o se arrepiente, el usuario B podrá hacer una evaluación neutra (0) al usuario A.</li>
</ul>
<p>Ten cuidado al hacer una oferta de intercambio, si luego no puedes cumplirla puedes recibir una evaluación neutra (0) que hará bajar tu factor de "seriedad" como coleccionista.</p>
<p>No ofrezcas el mismo cromo a varios usuarios, u puedes encontrarte varias evaluaciones neutras.</p>
</blockquote>
<p><em>C02. ¿Cuanto dura una oferta de intercambio?</em></p>
<blockquote>Cuando ofreces cromos a otro usuario, la oferta puede ser aceptada durante <strong>24 horas</strong>. Pasado ese tiempo puedes ofrecer tus cromos a otro usuario diferente.</blockquote>
<p><em>C03. Me llegan muchas ofertas a cambio de sellos o sobres. ¿Se puede decir en algun sitio que solo quiero cambiar por los cromos que me faltan?</em></p>
<blockquote>En la sección Mis Datos, dentro de &quot;Opciones y notificaciones&quot; puedes indicar que prefieres cambiar solo por tus faltas.</blockquote>
<br/>
<p><strong>E. Evaluaciones</strong></p>
<p><em>E01. Me han evaluado por equivocación y me han puesto un negativo, ¿como se puede borrar?</em></p>
<blockquote>Quien te ha puesto el negativo te lo puede quitar, solo tiene que volver a evaluarte de forma positiva y el negativo desaparecerá automáticamente.</blockquote>
<p><em>E02. ¿Por qué si me acaban de evaluar positivamente  mi contador de evaluaciones positivas sigue igual y no ha aumentado?</em></p>
<blockquote>  Si quien te acaba de evaluar lo ha hecho ya tres veces este año, no te volverá a sumar un positivo hasta dentro de un año. En el caso de colaboradores esta cifra son 10 evaluaciones anuales por usuario como límite.</blockquote>
<p><em>E03. ¿Por qué me han desparecido de repente alguna evaluación?</em></p>
<blockquote>  Cuando un usuario es expulsado de la comunidad se borran las evaluaciones positivas que dió a los demás, esta situación no es habitual, pero puedes haber perdido alguna evaluación que te diese el usuario que acabamos de expulsar.</br>
Esta situación también ocurre cuando alguien pide su baja voluntaria.</blockquote>
<p><em>E04. ¿Cuáles son los motivos para hacer una valoración neutra a otro usuario?</em></p>
<blockquote>
A. No respetar la regla de las 24 horas de reserva de una oferta</br>
B. Hacer el envío con retraso.</br>
C. Cromos en estado peor del acordado.
</blockquote>
<p><em>E05. Me han evaluado con un positivo doble, ¿a que se debe?</em></p>
<blockquote>Un usuarios Vip plata o superior puede hacer una evaluación doble como premio, a un usuario que en su último intercambio
haya destacado por su amabilidad, generosidad, puntualidad, etc.</blockquote>
<br/>
<p><strong>S. Seriedad</strong></p>
<p><em>S01. ¿Como se pierde seriedad?</em></p>
<blockquote>La seriedad se pierde si un usuario te pone una evaluación neutra con valor 0. Estas evaluaciones no suben al contador de evaluaciones pero afectan a tu seriedad como coleccionista.</blockquote>
<p><em>S02. ¿Para que sirve la seriedad?</em></p>
<blockquote>Los coleccionistas con valor alto de seriedad aparecerán en un lugar preferente en el buscador de intercambios.</blockquote>
<blockquote>Por el contrario, los coleccionistas con valor bajo de seriedad, apareceran en un lugar menos importante en el buscador de intercambios y puede que incluso no lleguen a aparecer bajo algunas circusntancias.</blockquote>
<blockquote>Por lo general un coleccionista con un valor alto de seriedad será más confiable que otro con un valor bajo y recibirá más peticiones de intercambio.</blockquote>
<p><em>S01. ¿Como recupero seriedad?</em></p>
<blockquote>Cada mes recuperarás un punto de seriedad si durante el mes anterior, has recibido al menos 3 evaluaciones positivas y no has recibido ninguna negativa ni neutra.</blockquote>
